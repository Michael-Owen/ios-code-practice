define("no320/event", function(require, exports, module) {
    /**
     * Creates a domready event function used to callback when page onload
     *
     * @private
     */
    
/**
 * id  tag 
 * each
 */ 

	// Used for trimming whitespace
	rtrim = /^(\s|\u00A0)+|(\s|\u00A0)+$/g,


	module.exports ={
		trim: function( text ) {
			return (text || "").replace( rtrim, "" );
		}
		
	};

});
